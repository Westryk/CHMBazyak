def get_precision(n_decimals):
    """Возвращает вес разряда (например, для 2 знаков это 0.01)"""
    return 10**(-n_decimals)

# --- УПРАЖНЕНИЕ 1 ---
# Округлить числа до 2, 3 и 4 знаков после запятой
task1 = [3.009982, 24.00551, 21.161728]
precisions = [2, 3, 4]

print("Упражнение 1 (Округление):")
for num in task1:
    results = [round(num, p) for p in precisions]
    print(f"Число {num}: до 2 зн. -> {results[0]}, до 3 зн. -> {results[1]}, до 4 зн. -> {results[2]}")

# --- УПРАЖНЕНИЕ 2 ---
# Найти погрешности, если все цифры верны в СТРОГОМ смысле
# Правило из учебника: погрешность не превышает ПОЛОВИНЫ единицы последнего разряда
print("\nУпражнение 2 (Предельные погрешности):")
tasks2 = [36.7, 2.489, 31.010, 0.031]

for val in tasks2:
    # Считаем количество знаков после запятой
    s_val = str(val).rstrip('0') if val != 31.010 else "31.010" # Учитываем значащий нуль
    decimals = len(s_val.split('.')[1]) if '.' in s_val else 0
    
    # Вес последнего разряда
    unit = get_precision(decimals)
    # В строгом смысле ошибка = 0.5 * unit
    delta_abs = 0.5 * unit
    delta_rel = (delta_abs / val) * 100
    
    print(f"Для {val}: Δx = {delta_abs:.5f}, δx = {delta_rel:.4f}%")

# --- УПРАЖНЕНИЕ 3 ---
# Округлить до сотых и найти количество верных цифр в СТРОГОМ смысле
print("\nУпражнение 3 (Округление до сотых):")
tasks3 = [0.310, 3.495, 24.3790]

for x in tasks3:
    x1 = round(x, 2)
    # Погрешность округления
    delta_round = abs(x - x1)
    # Исходная погрешность (т.к. цифры были верны в строгом смысле)
    # Для 0.310 это 0.001/2 = 0.0005 и т.д
    # В данном случае для краткости считаем, что цифры после сотых теперь сомнительны
    print(f"Число {x} -> {x1}. Погрешность округления: {delta_round:.4f}")