def f(x):
    return x**3 + 3*x**2 - 24*x + 1

def df(x):
    # Производная функции для метода касательных
    return 3*x**2 + 6*x - 24

def combined_method():
    # Начальные границы отрезка
    a = 0.0
    b = 1.0
    eps = 0.000001
    
    iteration = 0
    
    # По правилу метода: 
    # С того конца, где f(x) * f''(x) > 0, идет метод касательных.
    # С другого конца идет метод хорд.
    # Для нашей функции на [0, 1] f''(x) = 6x + 6 (всегда > 0)
    # f(0) = 1 (положительно), значит касательная пойдет из точки a=0.
    
    while abs(b - a) > eps:
        iteration += 1
        
        # Метод касательных (уточняем сторону 'a')
        a = a - f(a) / df(a)
        
        # Метод хорд (уточняем сторону 'b')
        b = b - (f(b) * (b - a)) / (f(b) - f(a))
        
        print(f"Итерация {iteration}: отрезок [{a:.8f}, {b:.8f}]")

    x_root = (a + b) / 2
    print("-" * 30)
    print(f"Корень найден за {iteration} итераций")
    print(f"x = {x_root:.7f}")

if __name__ == "__main__":
    combined_method()