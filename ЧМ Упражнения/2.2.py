import math

# 1. Определяем функцию
def f(x):
    return x * math.sin(x) - 1

# 2. Задаем начальные значения (из нашего анализа отрезка)
a = float(input("Введите a: "))
b = float(input("Введите b: "))
eps = float(input("Введите точность (eps): "))

# 3. Реализуем цикл по блок-схеме из рис. 2.6
while (b - a) > eps:
    c = (a + b) / 2
    
    if f(a) * f(c) < 0:
        b = c  # Корень в левой половине
    else:
        a = c  # Корень в правой половине

# 4. Вычисляем финальный результат
x = (a + b) / 2
delta = (b - a) / 2

# 5. Вывод (используем f-строки для красоты)
print(f"Корень x = {x:.5f}")
print(f"Погрешность = {delta:.5f}")